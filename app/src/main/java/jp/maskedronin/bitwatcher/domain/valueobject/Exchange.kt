        pairs.contains(pair)

    val cryptos: List<Currency>
        get() = pairs
            .flatMap { listOf(it.first, it.second) }
            .distinct()
            .filterNotNull()
            .filter { it.isCrypto }
            .sortedBy { it.getSymbol() }

    fun toInt(): Int = ordinal

    companion object {
        fun fromInt(value: Int): Exchange? = values().find { it.ordinal == value }
